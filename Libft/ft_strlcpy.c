#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t			length_src;
	unsigned int	i;

	length_src = ft_strlen(src);
	if (size == 0)
		return (length_src);
	i = 0;
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (length_src);
}
