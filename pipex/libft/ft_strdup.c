#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*temp;
	size_t	i;

	i = ft_strlen(s1);
	temp = malloc(sizeof(char) * (i + 1));
	if (!temp)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
